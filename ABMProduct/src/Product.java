    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         Conectar con = new Conectar();
        Connection cn= (Connection) con.conexion();
        
        int fila = jTable1.getSelectedRow();
        int id = Integer.parseInt(jTable1.getValueAt(fila,0).toString());
        
        int confirmar;
        
        try {
            confirmar = JOptionPane.showConfirmDialog(null,"¿Desea borrar el registro?");
            if(JOptionPane.OK_OPTION  == confirmar){
             String sql = "delete from productos where id_producto = "+id;
             PreparedStatement ps =(PreparedStatement) cn.prepareStatement(sql);
             JOptionPane.showMessageDialog(null,"El registro ha sido eliminado exitosamente","Operación Exitosa",JOptionPane.INFORMATION_MESSAGE);
             ps.executeUpdate();
             limpiar();
             mostrarDatosTabla("");
             jButton4.setEnabled(false);
             jButton3.setEnabled(false);
             jButton2.setEnabled(true);
                
            }
            else{
                   JOptionPane.showMessageDialog(null, "Eligió no eliminar el registro", "Operación cancelada", JOptionPane.INFORMATION_MESSAGE);
                mostrarDatosTabla("");
                jButton4.setEnabled(false);
                jButton3.setEnabled(false);
                jButton2.setEnabled(true);
                     }
        } catch (Exception e) {
            java.util.logging.Logger.getLogger(Product.class.getName()) .log(java.util.logging.Level.SEVERE,null,e);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

