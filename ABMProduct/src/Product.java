    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Conectar con = new Conectar();
        Connection cn= (Connection) con.conexion();
        
        
        try {
            //Si el item del combo box es 0 o alguno está vacio entonces faltan campos, sino, Guardo consulta
            if (jComboBox1.getSelectedIndex()== 0||"".equals(jDescripcion.getText()) || "".equals(jPrecio.getText()) || "".equals(jCantidad.getText())  ) {
                JOptionPane.showMessageDialog(null, "Faltan campos","Mensaje",JOptionPane.INFORMATION_MESSAGE);
            }else{
            PreparedStatement pst = (PreparedStatement)cn.prepareStatement("insert into productos (id_producto,categoria,descripcion,precio,cantidad) values(?,?,?,?,?)");
            pst.setInt(1, (int) (Math.random()*10000));
            pst.setString(2, (String) jComboBox1.getSelectedItem());
            pst.setString(3, jDescripcion.getText());
            pst.setString(4, jPrecio.getText());
            pst.setString(5, jCantidad.getText());
            pst.executeUpdate();
             JOptionPane.showMessageDialog(null, "Nuevo producto cargado","Mensaje",JOptionPane.INFORMATION_MESSAGE);
             mostrarDatosTabla("");
             jButton1.setEnabled(false);
             jButton2.setEnabled(true);
            
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

